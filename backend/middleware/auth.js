const jwt = require('jsonwebtoken');
const User = require('../models/User');

const auth = async (req, res, next) => {
  try {
    const token = req.header('Authorization')?.replace('Bearer ', '');
    
    if (!token) {
      return res.status(401).json({ 
        success: false, 
        message: 'No token provided, authorization denied' 
      });
    }

    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    const user = await User.findById(decoded.userId).select('-password');
    
    if (!user) {
      return res.status(401).json({ 
        success: false, 
        message: 'Token is not valid' 
      });
    }

    if (!user.isActive) {
      return res.status(401).json({ 
        success: false, 
        message: 'Account is deactivated' 
      });
    }

    req.user = user;
    next();
  } catch (error) {
    console.error('Auth middleware error:', error);
    res.status(401).json({ 
      success: false, 
      message: 'Token is not valid' 
    });
  }
};

const optionalAuth = async (req, res, next) => {
  try {
    const token = req.header('Authorization')?.replace('Bearer ', '');
    
    if (token) {
      const decoded = jwt.verify(token, process.env.JWT_SECRET);
      const user = await User.findById(decoded.userId).select('-password');
      
      if (user && user.isActive) {
        req.user = user;
      }
    }
    
    next();
  } catch (error) {
    // Continue without authentication for optional auth
    next();
  }
};

const requireSubscription = (requiredLevel) => {
  return (req, res, next) => {
    const subscriptionLevels = {
      'free': 0,
      'pro': 1,
      'enterprise': 2
    };

    const userLevel = subscriptionLevels[req.user.subscription] || 0;
    const requiredLevelValue = subscriptionLevels[requiredLevel] || 0;

    if (userLevel < requiredLevelValue) {
      return res.status(403).json({
        success: false,
        message: `This feature requires ${requiredLevel} subscription or higher`
      });
    }

    next();
  };
};

module.exports = {
  auth,
  optionalAuth,
  requireSubscription
};
